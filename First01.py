# โค้ดเวอร์ชันใหม่ของฉัน
# พัฒนาจากต้นแบบ โดย: เพิ่มการรับค่าจากผู้ใช้, เมนูเลือกรูปทรง (สี่เหลี่ยม/สามเหลี่ยม) และระบบตรวจสอบค่าลบ

print("=== โปรแกรมคำนวณพื้นที่อัจฉริยะ ===")
print("1. รูปสี่เหลี่ยมมุมฉาก")
print("2. รูปสามเหลี่ยม")

# รับตัวเลือกจากผู้ใช้
choice = input("กรุณาเลือกรูปทรงที่ต้องการคำนวณ (1 หรือ 2): ")
if choice == "1" or choice == "2":
    # รับค่าจากคีย์บอร์ดและแปลงเป็นทศนิยม (float) เพื่อรองรับเลขทศนิยม
    base_width = float(input("กรุณาใส่ความกว้าง / ความยาวฐาน: "))
    height_length = float(input("กรุณาใส่ความยาว / ความสูง: "))

    # ตรวจสอบเงื่อนไขดักจับข้อผิดพลาด (ต้องมากกว่า 0)
    if base_width <= 0 or height_length <= 0:
        print("❌ ข้อผิดพลาด: ค่าที่ป้อนต้องมากกว่า 0 เท่านั้น! กรุณาลองใหม่อีกครั้ง")
    else:
        # คำนวณตามเงื่อนไขที่เลือก
        if choice == "1":
            area = base_width * height_length
            print(f"📐 พื้นที่รูปสี่เหลี่ยมมุมฉากคือ: {area} ตารางหน่วย")
        elif choice == "2":
            area = 0.5 * base_width * height_length
            print(f"📐 พื้นที่รูปสามเหลี่ยมคือ: {area} ตารางหน่วย")
else:
    print("❌ ข้อผิดพลาด: ไม่มีเมนูที่คุณเลือก กรุณากรอกเฉพาะเลข 1 หรือ 2 เท่านั้น")

print("=================================")